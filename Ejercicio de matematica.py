""" Construye un programa donde el usuario ingrese dos numeros y estos a la vez se hagan las siguientes
operaciones: suma, resta, multiplicacion, division, multiplicacion exponencial y division entera"""

"""Suma +
   Resta - 
   Multiplicacion *
   Division /
   Multiplicacion Exponencial **
   Division Entera // """

numUno = int(input("digite un numero: = "))
numDos = int(input("digite un numero: = "))

suma = numUno + numDos
print (f"El resultado de la suma es:{suma}")

resta = numUno - numDos
print (f"El resultado de la resta es:{resta}")

multiplicacion = numUno * numDos
print (f"El resultado de la multiplicaion es:{multiplicacion}")

division = numUno / numDos
print (f"El resultado de la division es:{division}")

exponencial = numUno ** numDos
print (f"El resultado de la multiplicaion exponencial es:{exponencial}")

entero = numUno // numDos
print (f"El resultado de la division entera es:{entero}")











